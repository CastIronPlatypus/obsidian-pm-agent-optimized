import { describe, expect, it } from 'vitest'
import { appendYaml, isOldFormat, parseFrontmatter, stripAutoGeneratedContent } from './YamlParser'

describe('parseFrontmatter', () => {
  it('returns null frontmatter when the document has no fences', () => {
    const { frontmatter, body } = parseFrontmatter('just body text')
    expect(frontmatter).toBeNull()
    expect(body).toBe('just body text')
  })

  it('parses a fenced frontmatter block', () => {
    const doc = '---\ntitle: "Hello"\ncount: 3\n---\n\n# Body'
    const { frontmatter, body } = parseFrontmatter(doc)
    expect(frontmatter).toEqual({ title: 'Hello', count: 3 })
    expect(body).toBe('# Body')
  })

  it('returns the full content as body when the closing fence is missing', () => {
    const doc = '---\ntitle: "Hello"\n'
    const { frontmatter, body } = parseFrontmatter(doc)
    expect(frontmatter).toBeNull()
    expect(body).toBe(doc)
  })

  it('falls back to null on malformed yaml', () => {
    const doc = '---\n: : :\n---\nbody'
    const { frontmatter } = parseFrontmatter(doc)
    expect(frontmatter).toBeNull()
  })
})

describe('stripAutoGeneratedContent', () => {
  it('removes Project wiki-link lines', () => {
    expect(stripAutoGeneratedContent('Project: [[my-project|My Project]]')).toBe('')
  })

  it('removes Parent wiki-link lines', () => {
    expect(stripAutoGeneratedContent('Parent: [[parent-slug|Parent]]')).toBe('')
  })

  it('removes a trailing Subtasks section', () => {
    const body = 'Description goes here\n\n## Subtasks\n- [ ] [[sub|Sub]]'
    expect(stripAutoGeneratedContent(body)).toBe('Description goes here')
  })

  it('preserves user content when no auto-generated markers exist', () => {
    expect(stripAutoGeneratedContent('Plain description.')).toBe('Plain description.')
  })
})

describe('isOldFormat', () => {
  it('is true when tasks is a non-empty array and taskIds is absent', () => {
    expect(isOldFormat({ tasks: [{ id: 'a' }] })).toBe(true)
  })

  it('is false when taskIds is present even if tasks is also set', () => {
    expect(isOldFormat({ tasks: [{ id: 'a' }], taskIds: ['a'] })).toBe(false)
  })

  it('is false when tasks is empty', () => {
    expect(isOldFormat({ tasks: [] })).toBe(false)
  })

  it('is false when tasks is absent', () => {
    expect(isOldFormat({ title: 'Project' })).toBe(false)
  })
})

describe('appendYaml', () => {
  it('serializes primitive types with quoted strings and unquoted numbers/bools', () => {
    const lines: string[] = []
    appendYaml(lines, { s: 'hi', n: 2, b: true, nil: null }, 0)
    expect(lines).toEqual(['s: "hi"', 'n: 2', 'b: true', 'nil:'])
  })

  it('escapes quotes, backslashes, and newlines in strings', () => {
    const lines: string[] = []
    appendYaml(lines, { s: 'a "b"\n\\c' }, 0)
    expect(lines).toEqual(['s: "a \\"b\\"\\n\\\\c"'])
  })

  it('emits inline arrays for primitive items and empty arrays as []', () => {
    const lines: string[] = []
    appendYaml(lines, { list: ['a', 'b'], empty: [] }, 0)
    expect(lines).toEqual(['list: ["a", "b"]', 'empty: []'])
  })

  it('emits nested blocks for arrays of objects', () => {
    const lines: string[] = []
    appendYaml(
      lines,
      {
        items: [
          { k: 'v1', n: 1 },
          { k: 'v2', n: 2 }
        ]
      },
      0
    )
    expect(lines).toEqual(['items:', '  - k: "v1"', '    n: 1', '  - k: "v2"', '    n: 2'])
  })

  it('indents nested objects by the given amount', () => {
    const lines: string[] = []
    appendYaml(lines, { outer: { inner: 'x' } }, 0)
    expect(lines).toEqual(['outer:', '  inner: "x"'])
  })

  it('serializes Date values as ISO strings, not empty objects', () => {
    // Obsidian's parseYaml coerces unquoted YAML timestamps (as real TaskNotes
    // files write dates) into Date objects. Without special handling these would
    // fall through to the object branch and serialize as `{}`, dropping the value.
    const lines: string[] = []
    appendYaml(lines, { when: new Date('2026-07-08T15:04:19.713Z') }, 0)
    expect(lines).toEqual(['when: 2026-07-08T15:04:19.713Z'])
  })

  it('handles null entries inside arrays without crashing', () => {
    const lines: string[] = []
    appendYaml(lines, { list: [null, 'a'] }, 0)
    expect(lines).toEqual(['list: [null, "a"]'])
  })

  it('drops undefined array entries so they cannot emit a stray comma', () => {
    // A shared TaskNotes note with no PM `id` yields `task.id === undefined`, which
    // once reached `taskIds` and serialized as an empty element (`[..., , ]`),
    // breaking the project file's YAML. Undefined entries must simply vanish.
    const lines: string[] = []
    appendYaml(lines, { taskIds: ['a', undefined, 'b'] }, 0)
    expect(lines).toEqual(['taskIds: ["a", "b"]'])
  })
})
